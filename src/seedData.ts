export const getSeedSnapshots = (): MonthlySnapshot[] => {
  const snapshots: MonthlySnapshot[] = [];

  months.forEach((month, mIdx) => {
    const categories: any[] = [
      { type: '주식', items: [] },
      { type: '코인', items: [] },
      { type: '주택청약', items: [] },
      { type: '부동산', items: [] },
      { type: '연금', items: [] },
      { type: '적금', items: [] },
    ];

    rawData.forEach(([type, name, owner, values]) => {
      const principal = values[mIdx * 2];
      const currentValue = values[mIdx * 2 + 1];
      
      if (principal !== 0 || currentValue !== 0) {
        const cat = categories.find(c => c.type === type);
        if (cat) {
          cat.items.push({
            id: crypto.randomUUID(),
            name,
            owner: owner || undefined,
            principal,
            currentValue,
            deposit: 0,
            withdrawal: 0
          });
        }
      }
    });

    snapshots.push({
      id: crypto.randomUUID(),
      date: month,
      categories
    });
  });

  return snapshots;
};
